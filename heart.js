function showSubCatalog(catalogType) {
    const mainCatalog = document.getElementById('mainCatalog');
    const subCatalogs = document.getElementById('subCatalogs');
    const backBtnContainer = document.querySelector('.back-btn-container');
    const mainCatalogtxt = document.getElementById('mainCatalogtxt'); // Текст основного каталога
    const subCatalogtxt = document.getElementById('subCatalogtxt'); // Текст подкаталога
    
    // Скрываем основной каталог и его текст
    mainCatalog.style.display = 'none';
    mainCatalogtxt.style.display = 'none'; // Скрываем текст основного каталога
    
    // Отображаем маленькие каталоги
    subCatalogs.style.display = 'grid';
    
    // Показываем текст подкаталога
    subCatalogtxt.style.display = 'block'; // Показываем текст для подкаталога
    
    // Показываем кнопку "Назад"
    backBtnContainer.style.display = 'block';
    
    // Очищаем предыдущие маленькие каталоги
    subCatalogs.innerHTML = '';
    
    // Получаем данные для выбранного каталога
    const catalogItems = subCatalogsData[catalogType];
    
    // Добавляем элементы маленького каталога
    catalogItems.forEach(item => {
        const itemDiv = document.createElement('div');
        itemDiv.classList.add('sub-catalog-item');
        
        itemDiv.innerHTML = ` 
            <img src="${item.img}" alt="${item.name}" class="sub-catalog-item-img">
            <p>${item.name}</p>
            <p class="price">${item.price}</p>
            <a href="https://t.me/r0nnyq" class="order-btn">Заказать</a>
        `;
        
        subCatalogs.appendChild(itemDiv);
    });
}

